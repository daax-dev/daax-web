export interface NameGeneratorOptions {
  existingNames?: string[];
  maxAttempts?: number;
}

/**
 * Generates a 4-character random suffix using cryptographically secure randomness.
 *
 * Uses crypto.getRandomValues for better randomness than Math.random(),
 * avoiding predictable suffixes when Math.random() returns edge-case values.
 *
 * @returns A 4-character lowercase alphanumeric string (e.g., "a3f9", "x7k2")
 *
 * @example
 * generateSuffix() // Returns "a3f9"
 * generateSuffix() // Returns "k7x2"
 */
function generateSuffix(): string {
  const chars = "0123456789abcdefghijklmnopqrstuvwxyz";
  const array = new Uint8Array(4);
  crypto.getRandomValues(array);
  return Array.from(array, (byte) => chars[byte % chars.length]).join("");
}

/**
 * Generate a cryptographically secure random index in the range [0, length).
 * Uses crypto.getRandomValues for consistent randomness quality throughout
 * the name generation process.
 */
function getSecureRandomIndex(length: number): number {
  const array = new Uint32Array(1);
  crypto.getRandomValues(array);
  return array[0] % length;
}

/**
 * Generate a random adjective-noun name with short hash suffix
 * Format: adjective-noun-xxxx (e.g., swift-falcon-a3f9)
 * Uses cryptographically secure randomness for all selections.
 */
export function generateName(): string {
  const adjective = ADJECTIVES[getSecureRandomIndex(ADJECTIVES.length)];
  const noun = NOUNS[getSecureRandomIndex(NOUNS.length)];
  const suffix = generateSuffix();
  return `${adjective}-${noun}-${suffix}`;
}

/**
 * Generate a unique name that doesn't collide with existing names
 * With the 4-char suffix, collisions are extremely rare (1 in 1.6M per base name)
 */
export function generateUniqueName(options: NameGeneratorOptions = {}): string {
  const { existingNames = [], maxAttempts = 10 } = options;
  const existingSet = new Set(existingNames.map((n) => n.toLowerCase()));

  for (let i = 0; i < maxAttempts; i++) {
    const name = generateName();
    if (!existingSet.has(name)) {
      return name;
    }
  }

  // Fallback: use timestamp if somehow still colliding
  return `${generateName()}-${Date.now().toString(36)}`;
}

// NOTE: For comprehensive branch name validation (including security checks),
