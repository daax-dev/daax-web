// This simplified function is kept for backward compatibility with name generation.
/**
 * Check if a generated name is valid for use as a git branch name.
 * This is a simplified check for names generated by this module.
 * For external input validation, use isValidBranchName from worktree-manager.
 */
export function isValidGeneratedName(name: string): boolean {
  // Git branch naming rules (simplified for generated names)
  // - Must start with a letter
  // - Can contain letters, numbers, and hyphens
  // - Cannot have consecutive hyphens
  // - Must end with a letter or number
  // This pattern also allows single-character names (e.g., "a") for consistency
  return /^[a-z](?:[a-z0-9-]*[a-z0-9])?$/.test(name) && !name.includes("--");
}

/**
 * Get the total number of possible unique combinations
 * Base combinations × suffix combinations (36^4)
 */
export function getTotalCombinations(): number {
  const baseCombinations = ADJECTIVES.length * NOUNS.length;
  const suffixCombinations = Math.pow(36, 4); // 4-char base36 = 1,679,616
  return baseCombinations * suffixCombinations; // ~4.1 billion
}
